str = "Good Lad"

# change to lower case
print(str.lower())

# change to Upper case
print(str.upper())

# check for cases
# UPPER
print(str.upper().isupper())
# lower
print(str.lower().islower())

# length of the string
print(len(str))

# Access each charecter
print(str[6])

# Gives index of the value
print(str.index("a"))

# Replace string
print(str.replace("Good", "Fuck it!!!!"))